package com.tns.day1;

public class DataTypesDemo {

	public static void main(String[] args) {
		int value1=9/2;
		float value2 = 101f / 61f;
		double value3 = 10d / 6d;
		float value4 = 5/2.5f; //float division
		
		System.out.println("value1 is"+value1);//4,
		System.out.println("value2 is"+value2);//1.6557,
		System.out.println("value 3  is "+value3);//1.666,
		System.out.println("value4 is"+value4);//2.0

	}

}
